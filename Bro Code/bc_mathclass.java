
class bc_mathclass {

    public static void main(String[] args) {
        System.out.println("Math classes : ");
        System.out.println("PI value = " + Math.PI);
        System.out.println("E value = " + Math.E);
        System.out.println("Minimum of 8 and 4 = " + Math.min(8, 4));
        System.out.println("Maximum of 8 and 4 = " + Math.max(8, 4));
        System.out.println("2 power 3 = " + Math.pow(2, 3));
        System.out.println("absolute value = " + Math.abs(-7));
        System.out.println("Rounded of 5.6 = " + Math.round(5.6));
        System.out.println("Ceil of 5.6 = " + Math.ceil(5.6));
        System.out.println("Floor of 5.6 = " + Math.floor(5.6));
        System.out.println("sqrt of 36 = " + Math.sqrt(36));

    }
}
